#include <unistd.h>

int	ft_atoi(char *str)
{
	int	num;
	int	s;

	s = 1;
	num = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n'
		|| *str == 'v' || *str == '\f' || *str == '\r' )
		str++;
	while ((*str == '-') || (*str == '+'))
	{
		if (*str == '-')
			s = -s;
		str++;
	}
	while (*str < 58 && *str > 47)
	{
		num = (num * 10) + (*str - '0');
		str++;
	}
	return (num * s);
}
